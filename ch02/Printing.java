/* Programa exibe os numeros de 1 a 4 na mesma linha */

public class Printing
{
		public static void main(String args[])
		{
				System.out.print("1 ");
				System.out.print("2 ");
				System.out.print("3 ");
				System.out.printf("%d ", 4);
				System.out.println();
		}
}
